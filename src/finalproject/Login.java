    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql;
        try{
            stat=con.createStatement();
            sql = "select * from pegawai where username_pegawai = '" 
                    + jTextField1.getText() + "' and password_pegawai = '" 
                    + jTextField2.getText() + "';";
            res=stat.executeQuery(sql);
            if(res.next())
            {
                pegawai = res.getInt("id_pegawai");
                jabatan = res.getInt("id_jabatan");
                
                if(jTextField1.getText().equals(res.getString("username_pegawai")) && jTextField2.getText().equals(res.getString("password_pegawai")))
                {
                JOptionPane.showMessageDialog(null,"Login Berhasil");
                sql = "insert into transaksi value(null,"
                        + this.pegawai + ",now());";
                pst = con.prepareStatement(sql);
                pst.execute();
                dispose();
                if(jabatan == 1){
                    new Kasir(pegawai).setVisible(true);
                    }
                else if(jabatan == 2){
                    new Gudang(pegawai).setVisible(true);
                }
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"Login Gagal");
                }
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,"Terjadi Permasalahan di "+e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

